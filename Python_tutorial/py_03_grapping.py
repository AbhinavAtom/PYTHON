x = 255
#Convert decimal to other number system
print(bin(x))
print(oct(x))
print(hex(x))

#Show number in decimal number system(no conversion required)
print(0b11111111)
print(0o377)
print(0xff)

#Complex Numbers
#SYNTAX___ complex(real, imaginary)
z = complex(1, -4)
print(z)
print(z.real)
print(z.imag)
